def sumar(num1,num2):
	print(f'El resultado de la suma es: {num1+num2}')

def resta(num1,num2):
	print(f'El resultado de la suma es: {num1-num2}')

def multiplicar(num1,num2):
	print(f'El resultado de la suma es: {num1*num2}')

def dividir(num1,num2):
	print(f'El resultado de la suma es: {num1/num2}')

def potencia(num1,num2):
	print(f'El resultado de la suma es: {num1**num2}')

def redondear(num1):
	print(f'El resultado de la suma es: {round(num1)}')

	